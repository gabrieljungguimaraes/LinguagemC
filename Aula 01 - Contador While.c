int main()
{
    int contador = 10; //inicia o contador !
    while (contador >= 1){ //usamos while para definir a condição !
        printf("\n %d", contador ); // imprimi a variavel contador !
        contador-- ; // repetição Contador -1 !
    }

    return 0;
}

//Resumo da aula:
//while: repete enquanto a condição for verdadeira.
//do...while: repete após executar uma vez, mesmo que a condição seja falsa depois.
//for: usado para contagem definida (ex: de 1 a 10).
